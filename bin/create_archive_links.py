def main():
    for link in links:
        writable = {
          'title': link['headline'],
          'date': link['date'],
          'tags': ['archive', 'link'],
          'extUrl': link['url']
        }
        with open('src/render/links/' + slugify(link['headline']) + '.html', 'w') as f:
            f.write('---\n')
            yaml.dump(writable, f, default_flow_style=False, width=10000)
            f.write('---\n')


if __name__ == '__main__':
    main()